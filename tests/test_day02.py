import aoc2025.day02 as d02
import pytest


def test_part1_get_invalid_ids_brute_force():
    ranges = [
        [11, 22],
        [95, 115],
        [998, 1012],
        [1188511880, 1188511890],
        [222220, 222224],
        [1698522, 1698528],
        [446443, 446449],
        [38593856, 38593862],
        [565653, 565659],
        [824824821, 824824827],
        [2121212118, 2121212124],
    ]

    assert d02.part1_get_invalid_ids_brute_force(ranges) == [
        11,
        22,
        99,
        1010,
        1188511885,
        222222,
        446446,
        38593859,
    ]


@pytest.mark.parametrize(
    "num,expected",
    [
        (2, [1, 2]),
        (3, [1, 3]),
        (4, [1, 2, 4]),
        (5, [1, 5]),
        (10, [1, 2, 5, 10]),
        (16, [1, 2, 4, 8, 16]),
    ],
)
def test_factors(num, expected):
    assert d02.factors(num) == expected


